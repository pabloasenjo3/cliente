enum FunctionalGroup {
  // By priority:
  acid, // =O, -OH
  carboxyl, // -COOH non-principal acid
  amide, // =O, -NH2
  carbamoyl, // -CONH2 non-principal amide
  nitrile, // ≡N
  cyanide, // -CN non-principal nitrile
  aldehyde, // =O, -H
  ketone, // =O
  alcohol, // -OH
  amine, // -NH2
  ether, // -O-R'
  alkene, // = double bond
  alkyne, // ≡ triple bond
  nitro, // -NO2
  bromine, // -Br
  chlorine, // -Cl
  fluorine, // -F
  iodine, // -I
  radical, // -R
  hydrogen // -H
}
